using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace GitCalc
{
    class Calculations
    {
        public static double Add(double a, double b)
        {
            return a + b;
        }

        public static double Minus(double a, double b)
        {
            return a - b;
        }

        public static double Times(double a, double b)
        {
            return a * b;
        }
    }

}
